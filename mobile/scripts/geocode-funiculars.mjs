// Focused re-geocode of the 4 funiculars (the main run missed most — generic
// station names). Patches the 'fun' entry in constants/transitMap.ts.
import { readFileSync, writeFileSync } from 'fs';
const UA = 'HelpMeNapoli/1.0 (erikholzer1@gmail.com)';
const BBOX = { latMin: 40.77, latMax: 40.92, lonMin: 14.08, lonMax: 14.35 };
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

const FUNS = [
  { name: 'Centrale', lower: ['Stazione Augusteo Napoli', 'Funicolare Centrale Augusteo Napoli', 'Augusteo, San Ferdinando, Napoli'], upper: ['Piazza Fuga, Vomero, Napoli', 'Funicolare Centrale Fuga Napoli'] },
  { name: 'Chiaia', lower: ['Parco Margherita, Napoli', 'Funicolare di Chiaia, Napoli'], upper: ['Via Cimarosa, Vomero, Napoli', 'Cimarosa funicolare Napoli'] },
  { name: 'Montesanto', lower: ['Stazione Montesanto, Napoli'], upper: ['Via Morghen, Vomero, Napoli', 'Morghen funicolare Napoli'] },
  { name: 'Mergellina', lower: ['Mergellina, Napoli'], upper: ['Via Manzoni, Posillipo, Napoli', 'Manzoni funicolare Napoli'] },
];

async function geocode(queries) {
  for (const q of queries) {
    try {
      const url = 'https://nominatim.openstreetmap.org/search?format=jsonv2&limit=1&countrycodes=it&q=' + encodeURIComponent(q);
      const r = await fetch(url, { headers: { 'User-Agent': UA }, signal: AbortSignal.timeout(15000) });
      await sleep(1100);
      if (!r.ok) continue;
      const d = await r.json();
      if (!d.length) continue;
      const lat = parseFloat(d[0].lat), lng = parseFloat(d[0].lon);
      if (lat >= BBOX.latMin && lat <= BBOX.latMax && lng >= BBOX.lonMin && lng <= BBOX.lonMax)
        return { lat: +lat.toFixed(5), lng: +lng.toFixed(5) };
    } catch { await sleep(1100); }
  }
  return null;
}

async function main() {
  const segs = [];
  for (const f of FUNS) {
    const a = await geocode(f.lower);
    const b = await geocode(f.upper);
    console.log(`${f.name}: lower ${a ? a.lat + ',' + a.lng : 'MISS'} | upper ${b ? b.lat + ',' + b.lng : 'MISS'}`);
    if (a && b) segs.push([{ name: f.name, lat: a.lat, lng: a.lng }, { name: f.name + ' (top)', lat: b.lat, lng: b.lng }]);
  }
  // patch transitMap.ts
  const file = 'constants/transitMap.ts';
  const txt = readFileSync(file, 'utf8');
  const marker = 'TransitMapLine[] = ';
  const start = txt.indexOf(marker) + marker.length;
  const arr = JSON.parse(txt.slice(start, txt.lastIndexOf(']') + 1));
  const fun = arr.find((l) => l.id === 'fun');
  fun.segments = segs;
  delete fun.stations;
  const body =
    '// AUTO-GENERATED by scripts/geocode-stations.mjs — real OSM coordinates.\n' +
    '// Re-run that script to refresh. Do not hand-edit coordinates from memory.\n\n' +
    'export type Station = { name: string; lat: number; lng: number };\n' +
    'export type TransitMapLine = {\n  id: string;\n  label: string;\n  color: string;\n' +
    '  stations?: Station[];\n  segments?: Station[][];\n};\n\n' +
    'export const transitMapLines: TransitMapLine[] = ' + JSON.stringify(arr, null, 2) + ';\n';
  writeFileSync(file, body, 'utf8');
  console.log(`\nPatched ${segs.length}/4 funiculars into ${file}`);
}
main().catch((e) => { console.error(e); process.exit(1); });
