#!/usr/bin/env node
// Geocodes Naples metro + funicular stations against OpenStreetMap (Nominatim)
// and writes constants/transitMap.ts — real coordinates, not from memory.
// Stations that don't resolve cleanly inside the Naples box are dropped (and
// logged) rather than guessed.  ~1 req/sec per Nominatim policy.

import { writeFileSync } from 'fs';

const UA = 'HelpMeNapoli/1.0 (erikholzer1@gmail.com)';
const BBOX = { latMin: 40.77, latMax: 40.92, lonMin: 14.08, lonMax: 14.35 };
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

// Line definitions — stations in geographic order. color = map line colour.
const LINES = [
  {
    id: 'l1', label: 'Metro Line 1', color: '#E0A100',
    q: (s) => [`${s} stazione metropolitana, Napoli`, `Metropolitana ${s}, Napoli`, `${s}, Napoli`],
    stations: ['Piscinola', 'Chiaiano', 'Frullone', 'Colli Aminei', 'Policlinico', 'Rione Alto',
      'Montedonzelli', "Medaglie d'Oro", 'Vanvitelli', 'Quattro Giornate', 'Salvator Rosa',
      'Materdei', 'Museo', 'Dante', 'Toledo', 'Municipio', 'Università', 'Duomo', 'Garibaldi'],
  },
  {
    id: 'l2', label: 'Metro Line 2', color: '#2E6FB7',
    q: (s) => [`Stazione ${s} Napoli`, `${s} stazione ferroviaria Napoli`, `${s}, Napoli`],
    stations: ['Gianturco', 'Napoli Piazza Garibaldi', 'Piazza Cavour', 'Montesanto',
      'Piazza Amedeo', 'Mergellina', 'Napoli Campi Flegrei', 'Bagnoli', 'Pozzuoli'],
  },
  {
    id: 'l6', label: 'Metro Line 6', color: '#C84E8F',
    q: (s) => [`${s} stazione metropolitana Napoli`, `${s}, Napoli`],
    stations: ['Mostra', 'Mergellina', 'Arco Mirelli', 'San Pasquale', 'Chiaia', 'Municipio'],
  },
  {
    id: 'fun', label: 'Funiculars', color: '#DE7C2B',
    q: (s) => [`Funicolare ${s} Napoli`, `${s} funicolare, Napoli`],
    // each funicular as a lower→upper pair (drawn as its own segment)
    segments: [
      ['Centrale Augusteo', 'Centrale Fuga'],
      ['Chiaia Parco Margherita', 'Chiaia Cimarosa'],
      ['Montesanto', 'Montesanto Morghen'],
      ['Mergellina', 'Mergellina Manzoni'],
    ],
  },
];

async function geocode(queries) {
  for (const q of queries) {
    try {
      const url = 'https://nominatim.openstreetmap.org/search?format=jsonv2&limit=1&countrycodes=it&q=' + encodeURIComponent(q);
      const r = await fetch(url, { headers: { 'User-Agent': UA }, signal: AbortSignal.timeout(15000) });
      await sleep(1100);
      if (!r.ok) continue;
      const d = await r.json();
      if (!d.length) continue;
      const lat = parseFloat(d[0].lat), lng = parseFloat(d[0].lon);
      if (lat >= BBOX.latMin && lat <= BBOX.latMax && lng >= BBOX.lonMin && lng <= BBOX.lonMax)
        return { lat: +lat.toFixed(5), lng: +lng.toFixed(5), label: d[0].display_name };
    } catch { await sleep(1100); }
  }
  return null;
}

async function main() {
  const out = [];
  for (const line of LINES) {
    if (line.segments) {
      // funiculars: each segment is its own two-point line
      const segs = [];
      for (const [lower, upper] of line.segments) {
        const a = await geocode(line.q(lower));
        const b = await geocode(line.q(upper));
        const display = lower.split(' ')[0];
        console.log(`[fun] ${display}: ${a ? 'OK' : 'MISS'} / ${b ? 'OK' : 'MISS'}`);
        const pts = [];
        if (a) pts.push({ name: display, lat: a.lat, lng: a.lng });
        if (b) pts.push({ name: display + ' (top)', lat: b.lat, lng: b.lng });
        if (pts.length === 2) segs.push(pts);
      }
      out.push({ id: line.id, label: line.label, color: line.color, segments: segs });
    } else {
      const stations = [];
      for (const s of line.stations) {
        const g = await geocode(line.q(s));
        console.log(`[${line.id}] ${s}: ${g ? 'OK ' + g.lat + ',' + g.lng : 'MISS'}`);
        if (g) stations.push({ name: s.replace('Napoli ', ''), lat: g.lat, lng: g.lng });
      }
      out.push({ id: line.id, label: line.label, color: line.color, stations });
    }
  }

  const body =
    '// AUTO-GENERATED by scripts/geocode-stations.mjs — real OSM coordinates.\n' +
    '// Re-run that script to refresh. Do not hand-edit coordinates from memory.\n\n' +
    'export type Station = { name: string; lat: number; lng: number };\n' +
    'export type TransitMapLine = {\n  id: string;\n  label: string;\n  color: string;\n' +
    '  stations?: Station[];\n  segments?: Station[][];\n};\n\n' +
    'export const transitMapLines: TransitMapLine[] = ' + JSON.stringify(out, null, 2) + ';\n';
  writeFileSync('constants/transitMap.ts', body, 'utf8');
  const n = out.reduce((a, l) => a + (l.stations?.length || 0) + (l.segments?.flat().length || 0), 0);
  console.log(`\nWrote constants/transitMap.ts (${n} located points)`);
}
main().catch((e) => { console.error(e); process.exit(1); });
